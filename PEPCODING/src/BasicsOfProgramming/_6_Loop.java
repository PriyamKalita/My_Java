package BasicsOfProgramming;

public class _6_Loop {
    public static void main(String[] args) {

        int i = 1;

        while ( i <= 9)
        {
            System.out.println(i);
            i = i + 1;
        }

        System.out.println("\n");

        for(int j = 1; j <= 9; j++)
        {
            System.out.println(i);
        }
        System.out.println("Loop End");
    }
}
