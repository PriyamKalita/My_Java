package com.company;

public class WHILELOOP {
    public static void main(String[] args) {



        int i = 0;
        while (i < 5) {
            System.out.println(i);
            i++;
        }



        //Do/While Loop
        int i2 = 0;
        do {
            System.out.println(i2);
            i2++;
        }
        while (i2 < 5);


    }
}
